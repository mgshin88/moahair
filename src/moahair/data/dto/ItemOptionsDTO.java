package moahair.data.dto;

public class ItemOptionsDTO {
	private int io_num;
	private int io_i_num;
	private String io_option;
	private String io_name;
	private String io_price;
	private String io_duration;
	
	
	public int getIo_num() {
		return io_num;
	}
	public void setIo_num(int io_num) {
		this.io_num = io_num;
	}
	public int getIo_i_num() {
		return io_i_num;
	}
	public void setIo_i_num(int io_i_num) {
		this.io_i_num = io_i_num;
	}
	public String getIo_option() {
		return io_option;
	}
	public void setIo_option(String io_option) {
		this.io_option = io_option;
	}
	public String getIo_name() {
		return io_name;
	}
	public void setIo_name(String io_name) {
		this.io_name = io_name;
	}
	public String getIo_price() {
		return io_price;
	}
	public void setIo_price(String io_price) {
		this.io_price = io_price;
	}
	public String getIo_duration() {
		return io_duration;
	}
	public void setIo_duration(String io_duration) {
		this.io_duration = io_duration;
	}
	
}
